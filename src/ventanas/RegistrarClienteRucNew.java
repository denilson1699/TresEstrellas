    private void boton_guardarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_guardarClienteActionPerformed
        String ruc_cliente ,razon_cliente,direccion_cliente,rubro,correoCliente,telf; int contador=0;
        // metodo para recuper la fecha selecionada de jdatechooser

        // recuperamos los valores ingresados en los jtextfield
        ruc_cliente      =txt_rucCliente.getText().trim().toUpperCase();
        razon_cliente =txt_razonCliente.getText().trim();
        direccion_cliente =txt_direcCliente.getText().trim();;
        correoCliente       =txt_correoCiente.getText().trim();
        telf   =txt_telfCliente.getText().trim();
        rubro=txt_rubroCliente1.getText().trim();
        // validamos qiue no encuentren campos en blanco
        if(ruc_cliente.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Falta ingresar  RUC ");
            txt_rucCliente.requestFocus();
            contador++;
        }
        if(razon_cliente.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Falta ingresar Razon ");
            txt_razonCliente.requestFocus();
            contador++;
        }
        if(rubro.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Falta ingresar Rubro ");
            txt_rubroCliente1.requestFocus();
            contador++;
        }
        if(direccion_cliente.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Falta ingresar Direcci칩n ");
            txt_direcCliente.requestFocus();
            contador++;
        }
        if(telf.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Falta ingresar Tel칠fono");
            txt_telfCliente.requestFocus();
            contador++;
        }
     
        // si todos los campos entan llenos
        if(contador==0){
        try
        {
            Connection cn=Conexion.Conectar();
            PreparedStatement pst= cn.prepareStatement("insert into tabla_clientesruc values (?,?,?,?,?,?,?)");
            pst.setString(1,ruc_cliente);
            pst.setString(2,razon_cliente);
            pst.setString(3,rubro);
            pst.setString(4,direccion_cliente);
            pst.setString(5,telf);
            pst.setString(6,correoCliente);
            pst.setString(7,jLabel_fecha.getText());
            pst.executeUpdate();
            cn.close();
            JOptionPane.showMessageDialog(null, "Cliente Registrado Exitosamente");
            this.dispose();

        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, "no se pudo registrar Cliente, contacte al administrador","",JOptionPane.ERROR_MESSAGE);
            System.err.println("error al registrar nuevo cliente: "+e);
        }
    }
        
    }//GEN-LAST:event_boton_guardarClienteActionPerformed

